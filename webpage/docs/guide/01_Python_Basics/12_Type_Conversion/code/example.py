# Complex Example 1
decimal_value = 3.14
string_value = "2.5"

# Convert string to float and perform addition
result_complex1 = decimal_value + float(string_value)
print(result_complex1)  # Output: 5.64


# Complex Example 2
integer_value = 42
boolean_value = True

# Convert boolean to string and concatenate with integer
result_complex2 = str(integer_value) + str(boolean_value)
print(result_complex2)  # Output: '42True'


# Complex Example 3
list_of_numbers = [1, 2, 3]
sum_of_numbers = 10

# Convert list to sum of integers and perform addition
result_complex3 = sum_of_numbers + sum(list_of_numbers)
print(result_complex3)  # Output: 16
